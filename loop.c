//using for loop for getting output 1 to 10
#include <stdio.h>
int main () {
    int i;
    for(int i=0;i<=10;i++){
        printf("%d\n",i);
    }
    return 0;
}
////using for while for getting output 1 to 10
#include <stdio.h>
int main (){
    int i =1;
    while(i<=10){
        printf("%d\n",i);
        i++;
    }
    return 0;
}
